// Tiny zero-dependency i18n. English source strings ARE the keys, so an
// untranslated string simply renders in English — nothing ever breaks or
// shows a raw key. The chosen language persists in localStorage and
// falls back to the browser's when unset. Components re-render on change
// via useLang() (useSyncExternalStore).
//
// To translate a string: wrap it in t("…") at the call site and add the
// same English text as a key under each language below.

import { useSyncExternalStore } from "react";

export type Lang = "en" | "tr" | "fr" | "de" | "it";

export const LANGS: Array<{ code: Lang; label: string }> = [
  { code: "en", label: "English" },
  { code: "tr", label: "Türkçe" },
  { code: "fr", label: "Français" },
  { code: "de", label: "Deutsch" },
  { code: "it", label: "Italiano" },
];

const STORE_KEY = "cuxdeck-lang";

function detect(): Lang {
  try {
    const saved = localStorage.getItem(STORE_KEY) as Lang | null;
    if (saved && LANGS.some((l) => l.code === saved)) return saved;
  } catch { /* private mode */ }
  const nav = (navigator.language || "en").slice(0, 2).toLowerCase();
  return (LANGS.find((l) => l.code === nav)?.code) || "en";
}

let current: Lang = detect();
const subs = new Set<() => void>();

export function getLang(): Lang { return current; }

export function setLang(l: Lang): void {
  current = l;
  try { localStorage.setItem(STORE_KEY, l); } catch { /* ignore */ }
  subs.forEach((f) => f());
}

function subscribe(cb: () => void): () => void {
  subs.add(cb);
  return () => { subs.delete(cb); };
}

// useLang subscribes a component to language changes. Read it once near
// the root so the whole tree re-renders when the language flips.
export function useLang(): Lang {
  return useSyncExternalStore(subscribe, getLang, getLang);
}

// t translates an English source string to the current language, or
// returns it unchanged (English, or any string without a translation).
export function t(s: string): string {
  if (current === "en") return s;
  return DICT[current][s] ?? s;
}

